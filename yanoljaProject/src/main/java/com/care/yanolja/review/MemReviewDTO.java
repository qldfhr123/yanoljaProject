package com.care.yanolja.review;

public class MemReviewDTO {

	private String userId;
	private String reseNum;
	private String hostName;
	private String roomName;
	private String content;	 
	private String writeDate;	 
	private String reviewPoint;
	
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getReseNum() {
		return reseNum;
	}
	public void setReseNum(String reseNum) {
		this.reseNum = reseNum;
	}
	public String getHostName() {
		return hostName;
	}
	public void setHostName(String hostName) {
		this.hostName = hostName;
	}
	public String getRoomName() {
		return roomName;
	}
	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWriteDate() {
		return writeDate;
	}
	public void setWriteDate(String writeDate) {
		this.writeDate = writeDate;
	}
	public String getReviewPoint() {
		return reviewPoint;
	}
	public void setReviewPoint(String reviewPoint) {
		this.reviewPoint = reviewPoint;
	}
	
	
	
	 
}
